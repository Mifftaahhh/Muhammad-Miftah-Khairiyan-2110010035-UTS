    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        textBahan.setText("""
                          Bahannya:
                          - 500 gram daging sapi tanpa lemak                          
                          - 150 gram es batu                          
                          - 1 sdm bawang putih gurih, haluskan                          
                          - 1 sdm bawang merah goreng, haluskan                          
                          - 1 sdt gula pasir                          
                          - 2 sdt garam                          
                          - 1 butir putih telur                          
                          - 1/2 sdt lada bubuk                          
                          - 100 gram tepung sagu                          
                          - 1/2 sdt baking powder
                          
                          Cara membuatnya :
                          1. Cuci bersih daging lalu potong kecil-kecil. Setelah itu masukkan di blender atau food processor dan haluskan.                          
                          2. Masukkan separuh es batu, bawang putih, dan bawang merah. Giling lagi sampai halus. Tambahkan garam. Giling kembali.                          
                          3. Tambahkan gula pasir, lada bubuk, putih telur, tapung sagu, dan baking powder. Tambahkan lagi sisa es batu. Giling sampai halus.                          
                          4. Pastikan adonan daging selalu dalam keadaan dingin (dengan memasukkan es batu secara bertahap). Giling sampai adonan bakso halus dan berwarna sedikit merah muda.                          
                          5. Didihkan air. Setelah air mendidih, kecilkan api. Pastikan air untuk merebus daging tidak dalam keadaan "bergejolak". Bentuk adonan bakso bulat-bulat, lalu masukkan ke dalam air rebusan. Lakukan sampai adonan habis.                          
                          6. Untuk menghasilkan bakso daging sapi yang matangnya sempurna dan merata, setelah bakso mengambang, besarkan apinya sedikit. Masak kembali selama kurang lebih 5 menit.                          
                          7. Siapkan wadah yang berisi air matang dingin (bisa ditambah es batu). Bakso yang sudah matang segera dipindahkan ke dalam air dingin. Tujuannya agar teksturnya lebih kenyal. Setelah itu tiriskan.
                          """);
    }//GEN-LAST:event_button3ActionPerformed

