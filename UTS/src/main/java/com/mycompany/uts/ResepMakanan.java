    private void button5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button5ActionPerformed
        textBahan.setText("""
                          Bahannya :
                          - 1½ kg buntut sapi
                          - 12 siung bawang putih
                          - 1½ sdm merica putih utuh
                          - 5 iris pala
                          - 7-8 biji cengkeh
                          - 8 siung bawang merah rajang
                          - 8 siung bawang putih rajang
                          - 2-3 tangkai seledri
                          - 3-4 tangkai daun bawang
                          - 3 buah tomat belah
                          - 4 3/4 sdt gula pasir
                          - Kentang yang sudah dikupas, dipotong dadu, dan direbus
                          - Wortel yang sudah dikupas, diiris, dan direbus
                          
                          Cara membuatnya :
                          1. Langkah pertama untuk membuat sop buntut adalah didihkan air lalu masukkan buntut sapi, rebus hingga setengah mendidih, kemudian angkat dan tiriskan. Tips untuk  mendapatkan kuah sop buntut yang jernih, sebaiknya buntut mentah tidak dicuci. Baca juga: Jangan Buang Pisang Hampir Busuk, Coba Resep Bolu Kukus Pisang
                          2. Setelah setengah direbus baru cuci dan bersihkan buntut sapi dengan air dingin mengalir. Setelah dicuci bersih, tambahkan air dingin secukupnya untuk merebus buntut sapi dengan api kecil. Rebus sampai mendidih kembali.
                          3. Sembari menunggu, kamu bisa haluskan bawang putih bersama merica, setelah halus dilanjutkan dengan menumis dengan minyak secukupnya. Jika warna bumbu sudah agak cokelat muda, tuangkan dua gelas air lalu didihkan hingga mendidih. Kemudian tuang dengan disaring ke dalam kuah buntut yang sudah mendidih. Langkah ini bisa disesuaikan dengan selera, jika ingin tidak disaring tidak masalah. Baca juga: Resep Praktis Chicken Piccata Milenesse, Ide Menu Makan Malam
                          4. Tambahkan irisan pala, cengkeh dan daun bawang serta seledri yang sudah disimpulkan. Sebaiknya seledri dimasukkan utuh dan tidak diiris tipis-tipis. Lalu tambahkan garam dan penyedap. Angkat seledri yang sudah layu, buang dan rebus buntut sapi sampai empuk. Baca juga: Resep Seared Rib Eye with Mushroom Sauce, Steak ala Hotel Bintang 5 di Bali
                          5. Agar menambah aroma, kamu bisa goreng bawang merah bawang putih yang sudah diiris tipis, goreng hingga kecokelatan.  Remas-remas dan taburkan ke dalam kaldu dan tambahkan gula pasir. Jangan lupa untuk merasakan sop buntut dan menambah bumbu sesuai seleramu jika perlu.
                          6. Jika sudah hidangkan sop buntut dengan irisan tomat, kentang , wortel dan daun bawang. Nikmati sop buntut selagi hangat.                                      
                          """);
    }//GEN-LAST:event_button5ActionPerformed

